public class Point {
    //Atributos
    private double x;
    private double y;
    //Ctor
    public Point(double xi, double yi) {
        this.x = xi;
        this.y = yi;
    }
    //Metodos, getters en este caso
    public double getX() {
        return x;
    }
    public double getY() {
        return y;
    }
}

